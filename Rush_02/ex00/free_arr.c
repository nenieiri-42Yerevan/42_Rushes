#include "rush02.h"

void	free_arr(char **arr)
{
	int	i;

	i = -1;
	if (arr == 0)
		return ;
	while (arr[++i] != 0)
	{
		free(arr[i]);
		arr[i] = 0;
	}
	free(arr);
	arr = 0;
}
